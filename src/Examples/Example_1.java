public class Example_1 {
    public static void main(String[]args){
        int lineWidth=6;
        for (int i = 1; i <= lineWidth; i++){
            System.out.print("*");
        }
        System.out.print("\n");
        lineWidth=10;
        for (int i = 1; i <= lineWidth; i++){
            System.out.print("*");
        }
    }
}
